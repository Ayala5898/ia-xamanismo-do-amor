def gerar_conteudo_espiritual(tema, opcao):
    if opcao == "Legenda mística para Instagram":
        return f"🌟 Aho! Quando o tema é **{tema}**, o campo se abre. Que esta legenda traga cura, presença e conexão com o sagrado que habita em você. #XamanismoDoAmor"

    elif opcao == "Roteiro de carrossel espiritual":
        return f"""🌀 Slide 1: Título forte sobre **{tema}**
🌱 Slide 2: Uma pergunta que abre o coração
🔥 Slide 3: Ensinamento espiritual
🌙 Slide 4: Exemplo real ou simbólico
💧 Slide 5: Integração
🕯️ Slide 6: Rezo final
📲 Slide 7: Chamada para ação (salve e compartilhe)"""

    elif opcao == "Frase cerimonial para Story":
        return f"🕊️ “No silêncio de **{tema}**, a alma escuta o que a mente não ousa dizer.” 🌹"

    elif opcao == "Chamada para live ritualística":
        return f"""🌀 Esta live é um chamado.
🌿 Tema: **{tema}**
🕯️ Prepare seu altar, acenda sua vela. Vamos atravessar este portal juntas(os)."""

    elif opcao == "Conteúdo viral com campanha":
        return f"""🔥 Você já sentiu o chamado de **{tema}** mas adiou o primeiro passo?
Este é o sinal.  
📲 Marca alguém que precisa ler isso.
#RitualDoAmor #XamanismoVivo"""

    elif opcao == "Meditação guiada completa":
        return f"""🌬️ **Introdução**  
Bem-vinde ao espaço sagrado da tua alma. Hoje, mergulharemos no tema **{tema}** com presença e escuta amorosa.

💨 **Respiração**  
Inspira profundamente... segura... e solta devagar.  
Sente o corpo relaxar. Faz isso mais duas vezes.

🌌 **Visualização**  
Visualiza uma luz suave tocando teu coração. Essa luz se expande... e começa a te envolver por inteiro.

🌀 **Aprofundamento**  
Agora, caminha com essa luz até o lugar onde mora tua ferida ou tua dúvida sobre **{tema}**.  
Sente. Escuta. Observa o que tua alma quer mostrar.

🌿 **Retorno consciente**  
Vai voltando devagar, trazendo contigo só o que é bênção.  
Sente teus pés, tuas mãos, tua respiração.

✨ **Bênção final**  
Que o tema **{tema}** seja um portal de amor, clareza e reconexão contigo.  
Tua alma ouviu. Teu corpo acolheu. Está feito."""

    else:
        return "Essa opção ainda não foi configurada. Em breve estará disponível. 🌙"
